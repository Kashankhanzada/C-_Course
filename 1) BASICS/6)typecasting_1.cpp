#include <iostream>;
using namespace std;

int main(){

	int a1 = 5;
	cout << a1;
	
	cout<<"\n";
	
	//here we type cast a number 
	int a = 'a';
	cout << a;

	cout<<"\n";

	//similarly
	char b = 'b';
	cout << b;

	cout<<"\n";
	
	char c = 78;
	cout << c;

	cout<<"\n";
	
	// here long value of integer is saved in a character  
	char r = 123456;
	cout << r;

	// unsigned only store positive numbers
	
	unsigned int positive = 112; 
	cout << positive ;
}
